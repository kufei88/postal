package com.jishengsoft.pojo;

public class Electrifys {
	private int id;
	private String electrify;
	private int rank;
	public String getElectrify() {
		return electrify;
	}
	public void setElectrify(String electrify) {
		this.electrify = electrify;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
